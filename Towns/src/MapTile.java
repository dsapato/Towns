import java.awt.Color;


public class MapTile {
	public int x;
	public int y;
	private final int SIZE = 10;
	public Color color;
	
	public MapTile(int xx, int yy, Color c){
		this.x = xx;
		this.y = yy;
		this.color = c;
	}
	
	public void draw(){
		Zen.setColor(color);
		Zen.fillRect(x,y, SIZE, SIZE);
	}
}
